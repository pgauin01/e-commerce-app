class Address {
  constructor(
    id,
    name,
    street,
    building,
    landmark,
    state,
    mobile,
    locationType
  ) {
    this.id = id;
    this.name = name;
    this.street = street;
    this.building = building;
    this.landmark = landmark;
    this.state = state;
    this.mobile = mobile;
    this.locationType = locationType;
  }
}

export default Address;
